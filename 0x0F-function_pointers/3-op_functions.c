#include "3-calc.h"

/**
  * op_add - addition
  * @a: first argument
  * @b: second argument
  * Return: sum of a and b
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - subtraction
  * @a: first argument
  * @b: second argument
  * Return: difference between a and b
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - multiplication
  * @a: first argument
  * @b: second argument
  * Return: product of a and b
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - division
  * @a: first argument
  * @b: second argument
  * Return: division of a by b
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - modulus
  * @a: first argument
  * @b: second argument
  * Return: modulus of a and b
  */
int op_mod(int a, int b)
{
	return (a % b);
}
